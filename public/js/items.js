    function showPinkUniform(imgs) {
        $("#pink-uniform").hide();

        var expandPinkUniform = document.getElementById("expandedPinkUniform");

        expandPinkUniform.src = imgs.src;

        expandPinkUniform.parentElement.style.display = "block";
    }

